    <h1>Groep</h1>
    <span style="text-align:center;font-style:italic;">“De vereniging Camerons-Duinzwervers is een padvindersgroep die beoogt een plezierige vrijetijdsbeleving voor de jeugd te organiseren waardoor een bijdrage wordt geleverd aan het vormen van de persoonlijkheid van haar leden.”</span>
    <p>Onze groep heeft zijn wortels in 1923 en is daarmee de oudste van de 32 groepen uit de regio Haarlem.
    De groep bestaat alleen uit jongens, verdeeld over drie leeftijdsgroepen of speltakken. Door de programmering
    en de toepassing van echte padvinderstechnieken als o.a. knopen, pionieren, oriëntatie en kamperen proberen 
    wij onze identiteit als traditionele padvindersgroep hoog te houden. Naast het gebruik van deze technieken
    vormen samenwerking, zelfstandigheid en hulpvaardigheid belangrijke elementen bij de activiteiten. Maar natuurlijk
    is er ook regelmatig tijd voor een potje voetbal of een spelletje ‘Britse Bulldog’. 
    </p>  
    <p>
    Voor de uitvoering van onze programma’s kunnen wij gebruik maken van een grote voorraad materiaal, een mooi clubhuis,
    een ruim eigen terrein en natuurlijk de inzet van tientallen vrijwilligers.
    </p>
