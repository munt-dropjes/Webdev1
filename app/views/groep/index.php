    <p>De twee grootste speltakken binnen onze groep zijn de <a href="/welpen">welpen</a> (7-11 jaar) en de <a href="/verkenners">verkenners</a> (11-16 jaar). 
    Naast de welpen en verkenners bestaat er binnen onze groep nog een speltak voor jongens van 16-18 jaar genaamd de <a href="/rowans">rowans</a>. 
    Zij draaien geheel zelfstandig spectaculair programma’s met de betere padvinderstechnieken. Na de rowans bestaat er 
    de mogelijkheid om door te stromen naar de welpenleiding of verkennersstaf.</p>
    <p>De <a href="/stam">stam</a>, bestaande uit oud-leiding en -staf, verleent hand-en-span-diensten met betrekking tot de programma’s, onderhoud van terrein en gebouwen.
    Ook tijdens de jaarlijkse nieuwjaars- en Sint Joris-opkomsten zijn zij, evenals de andere leden van de groep aanwezig.
    </p>
