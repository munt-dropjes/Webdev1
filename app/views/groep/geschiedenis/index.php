    <h1>Geschiedenis</h1>
    <h4>Een samengevatte geschiedenis</h4>
    <h3>De Camerons</h3>
    <p>
        De Camerons zijn, via enige afsplitsingen en een naamsverandering, voortgekomen uit groep 3. Deze groep werd op 15 augustus 1911 officieel opgericht. 
        Het was de eerste groep in Haarlem. De naam van groep 3 was destijds “het Groene Vendel”. In september 1923 werd groep 3 gesplitst en ontstond groep 4 
        “de Grijze Driehoek”. Deze datum wordt sindsdien aangehouden als oprichtingsdatum van onze groep. Met slechts 15 welpen en 10 verkenners werd onder 
        leiding van hopman Backx van Wereld begonnen. De horde onder de leiding van akela Jansen groeide snel, evenals de troep. In 1926 wonnen de Honden 
        van de Grijze Driehoek de regionale wedstrijden.
    </p>

    <p>
        In 1933 verliet groep 4 het oude clubhuis aan de Rolhuizen nabij de Nieuwe Gracht en vestigde zich aan de Tetterodeweg in Overveen. Hopman Radsma 
        richtte er een stam op en werd na enige tijd opgevolgd door hopman Vellinga. Onder zijn leiding werd er 1937 een bezoek gebracht aan de Wereldjamboree 
        in Vogelenzang. In dit zelfde jaar werd er begonnen met de bouw van een stamhok en het hordehol.
    </p>

    <p>
        Eind augustus 1938 werd groep 4 bij gebrek aan leiding ontbonden, nadat de hopman, de oubaas en de groepsleider de groep hadden verlaten. Gelukkig 
        kwam Oud-Vaandrig Klant in november om de groep weer op poten te zetten. Bij de eerste opkomst van de troep op 26 november 1938 werd besloten 
        de naam van de groep te wijzigen. Dit werd “de Camerons”, daar de das die de verkenners reeds droegen, overeen kwam met de tartan (familiekleuren) 
        van de Schotse clan Cameron of Erracht. De troep groeide geweldig onder hopman Klant en de horde draaide weer op volle toeren. In de zomer van 
        1939 gingen de verkenners op kamp naar Ommen. Vlak voor het einde hiervan dreigde mobilisatie voor de Tweede Wereldoorlog, waardoor het kamp 
        hals over kop moest worden afgebroken.
    </p>

    <p>
        Exact een jaar na de oprichting door hopman Klant, op 26 november 1939, nam hopman Molenaar de leiding van de troep over. Het eerste oorlogsjaar 
        konden er nog gewone opkomsten gedraaid worden, maar op 2 april 1941 werd, op last van de Duitsers, de Nederlandsche Padvindersbeweging ontbonden. 
        Na een huiszoeking bij hopman Molenaar werd de groepskas in beslag genomen en het troephuis verzegeld. Uniformen, insignes en alles wat met de 
        padvinderij te maken had moest worden ingeleverd. Officieel bestonden de Camerons niet meer.
    </p>

    <p> 
        Op 7 november 1943 hield oud-hopman Klant zijn trouwreceptie. Door de vele aanwezige leden werden plannen gesmeed om de groep weer op te richten. 
        Op 19 maart 1944 werd bij initiatiefnemer Flip van der Linden thuis de eerste opkomst gehouden. Uiteraard allemaal in het geheim aangezien de 
        padvinderij nog steeds verboden was.
    </p>

    <p>
        Dezelfde zomer ging de troep sinds jaren weer op kamp, naar Sassenheim. Ook dit kamp werd weer verstoord door de oorlog, nu door aangekondigde 
        razzia's. Na het kamp kwam er van de gewone programma's weinig terecht door de bepaling dat iedereen voor 8 uur 's avonds binnen moest zijn. Wel 
        heeft de troep zich verdienstelijk gemaakt door tijdens de oorlog en na de bevrijding te helpen bij evacuaties en voedselverstrekking door het Rode Kruis.
    </p>

    <p>
        In de zomer van 1946 kon er eindelijk weer een normaal kamp gehouden worden. Het duurde 14 dagen, wat vandaag de dag nog steeds de gewoonte is 
        bij de verkenners van de Camerons. De twee jaar oude, tweede, welpenhorde werd in 1947 overgedaan aan “de Rode Pijl”, omdat de Camerons haar 
        niet meer kon huisvesten. Daarentegen had “de Rode Pijl” wel ruimte, maar geen leden. Het kamp van de wereldbroederschap in 1948 vormde de basis 
        voor enkele nauwe contacten met een Deense groep. In de zomer van 1954 resulteerde dit in een kamp met de Denen in Fredericia. Het propellorblad 
        in het troeplokaal is een van de fraaie herinneringen hieraan. Het jaar ervoor, in 1953, werd het stenen clubhuis op de Tetterodeweg in gebruik genomen. 
        Dit staat er tot op de dag van vandaag nog. Het is nu in gebruik door het Waterleidingsbedrijf, als opslaggebouw. In september 1955 nam hopman Abspoel 
        de leiding over van hopman Akke van de Bunt. Dit gebeurde na hopman Van de Bunts tweede kamp samen met de Denen, dit maal in Ommen.
    </p>

    <p>
        In de zomer van dat jaar werd er tijdens het kamp, op kampeertterrein De Vrijenberg in Loenen, een permanente houten toren gebouwd. Deze toren, toen 
        “het Spechtenoog”, later “de Hoge Specht” genaamd, is tot 1988 blijven staan. De Valken wonnen in 1958 als eerste Camerons patrouille, de districtswedstrijden 
        en mochten naar de landelijke wedstrijden op Ada's Hoeve in Ommen. Tijdens het zomerkamp in het Drentse Sleen (1959) werd de eerste Nederlandse 
        paraboloïde gebouwd. Dit bouwwerk haalde zelfs de kranten en vele paraboloïdes zouden volgen.
    </p>

    <p>
        In 1964 zette de troep, onder leiding van Hopman Kees Wijdooge, zijn tenten op in Torquay, Engeland. Op deze Jamborette ontmoetten ze Chief Scout 
        MacLean (de hoogste Engelse padvinder) en werden ze ontvangen op het gemeentehuis van Plymouth door de burgemeester. Hierna teerde de groep 
        snel in en werd het noodzakelijk met de Duinzwervers op kamp te gaan. Wel naar het buitenland, naar Halteren in Duitsland. In 1966 was de troep 
        teruggelopen tot het historische dieptepunt van 6 verkenners.
    </p>

    <h3>De Duinzwervers</h3>
    <p>
    De Elfregi-Duinzwervers werden in 1945 opgericht en was een Elfregi-groep. Elfregi staat voor Elspeet Fredenshiem Giethoorn. 
    Dit waren de plaatsen waar doopsgezinde broederschapshuizen stonden en de doopsgezinde groepen hun eerste zomerkampen hielden. 
    Er waren direct 80 meisjes en 65 jongens die zich aanmeldden om lid te worden. Jongens en meisjes zouden niet gezamenlijk in een groep draaien. 
    Voor de jongens betekende dit dat er één welpenhorde ontstond en twee verkennerstroepen. De Elfregi jongens hadden hun eerste opkomst op 23 juni 1945. 
    Er waren voor deze opkomst slechts 8 jongens uitgenodigd, alle 8 wat ouder. Dit aantal groeide snel en op 3 juli werden de verkenners verdeeld in 2 groepen, groep 25 en groep 26.
    </p>

    <p>
    Groep 26 bleef tot de zomer van 1947 als “groep 26” door het leven gaan en veranderde toen de naam in “Duinzwervers”. 
    Jan Koeijers werd de Akela van groep 26, Jaap Aarts de Hopman. Men zou een donkerblauwe das met een witte “E” 5 cm boven de punt gaan dragen. 
    Het eerste zomerkamp van de Duinzwervers (toen nog groep 26 geheten) werd gehouden van 14 tot 19 augustus 1945 op Schoonenberg bij Driehuis. 
    Het aantal welpen groeide snel waardoor er in 1948 twee hordes waren. In eerste instantie was er nog geen clubhuis, 
    men zou de opkomsten vooral in Bloemendaal en langs het Visserspad houden. Na vele tijdelijke onderkomens kwam men in 1947 terecht in de doopsgezinde kerk aan de Frankestraat.
    </p>

    <p>
    In november 1951 kregen de Duinzwervers eindelijk een eigen clubhuis. Dit was het “Peushuis” in het centrum van Haarlem, in de Peuzelaarsteeg, op nummer 4. 
    Ook hadden de Elfregigroepen sinds 1953 een eigen buitenterrein aan de Zuidlaan in Aerdenhout-Bentveld, het Naaldenveld, waar ze echter geen onderkomen mochten bouwen.
    In 1953 wonnen de Duinzwervers voor het eerst de districtswedstrijden. In 1955 vertrokken de 27 verkenners en leiders van de Duinzwervers naar Maboge-en-Ardenne in de buurt van La Roche (België) 
    voor hun eerste buitenlandse zomerkamp. In 1957 vertrokken ze naar Forest Row in Engeland. Ook in 1959 hielden de verkenners hun zomerkamp wederom buiten Nederland, dit maal in het Duitse Idstein. 
    Het buitenterrein “Naaldenveld” werd dit jaar overgedragen aan Scouting Nederland.
    </p>

    <p>
    De Sperwer patrouille van de Duinzwervers hadden in 1960, net zoals het jaar ervoor (en het jaar erna), de districtswedstrijden gewonnen en mochten dus naar Ommen voor de landelijke wedstrijden. 
    Hier behaalde zij de eerste plaats! Deze patrouille won niet alleen het wisselschild, maar omdat de padvinderij in Nederland dat jaar 50 jaar bestond, 
    kregen de Sperwers ook een reis naar Schotland aangeboden door de organisatie. De Sperwers (en een begeleider) maakte een reis van 22 dagen. 
    De Duinzwervers besloten toen om de andere patrouilles de kans te geven op een internationaal kamp in Oostenrijk. Het zomerkamp van 1961 zou in Sint-Joris-Weert, België, gehouden worden.
    </p>

    <p>
    In 1962 kregen de welpen hun eigen clubhuis. De doopsgezinde kerk liet twee clubhuizen bouwen op de hoek van de Wagenweg en de Westelijke Randweg in het bos “Eindenhout” in Haarlem. 
    Één voor de meisjes Elfregigroep en één voor de Duinzwervers welpenhorde. Het clubhuis van de welpen heette “Wontolla”.
    </p>

    <p>
    In 1965 waren er (wederom) te weinig leidinggevenden. Het was dat jaar niet mogelijk alleen op zomerkamp te gaan en dus werd er een gezamenlijk kamp gehouden met de Camerons in Haltern, Duitsland. 
    De welpen gingen wel alleen op zomerkamp naar Bilthoven. In 1966 waren de problemen nog niet opgelost, integendeel. 
    Door het tekort aan leiding en alle problemen die dat met zich mee bracht, en de locatie van het troephuis was de troep inmiddels behoorlijk geslonken. 
    Hierdoor besloot men de verkenners tijdelijk met andere groepen mee te laten draaien. De troep ging dat jaar niet op zomerkamp. 
    Het zomerkamp van 1967, het laatste Duinzwervers welpen zomerkamp, werd gehouden in Wageningen. 
    De Impeesa horde (de naam van de Duinzwervers horde) zou wel als aparte horde binnen de Camerons-Duinzwervers blijven bestaan tot 1988.
    </p>

    <h3>De Camerons-Duinzwervers</h3>
    <p>
        Op 4 november 1967 werd op Slot Assumburg de fusie tussen de Camerons en de Duinzwervers een feit. De oude dassen werden geruild voor de nieuwe oranje-bruine groepsdas. 
        De groep bestond na de fusie uit de Impeesa-horde achter het-huis-met-de-witte-beelden aan de Wagenweg onder leiding van akela Timmerman. De Mafeking-horde van Akela 
        Groenewegen in het Lindehuis en de troep onder leiding van hopman Vlieg in het troephuis, beide aan de Tetterodeweg.
    </p>

    <p>
        In 1970 ging de naar 24 verkenners gegroeide troep naar Loenen, ditmaal om een kamp op palen te bouwen. Tenten op vlonders en de keukens en eettafels daaronder. Het jaar 
        1971 bracht na vele jaren van bestuurlijk touwtrekken eindelijk een nieuw clubhuis. De Mafeking-horde en de troep verhuisden naar Achnacarry II, het huidige onderkomen 
        aan de Duinlustweg in Overveen. Achnacarry II, vernoemd naar het kasteel van de clan-chief Cameron of Lochiel in Schotland. In de zomer ging de troep samen met de 
        Elfregi-groepen op kamp naar St. Jorisweert in België, waar een groot indianenkamp gehouden werd.
    </p>

    <p>
        In 1973, het jaar van het 50-jarig jubileum, vertrokken de verkenners, onder leiding van Hopman Wim van Gunsteren, naar Schotland voor het eerste Schotland zomerkamp. 
        Het werd een terrein aan de River Loy, vlakbij Erracht House en Tor Castles Ruin in Schotland. De reis naar het kamp verliep met bus en boot naar Baden Powell's House 
        in Londen, alwaar overnacht werd. Hierna met trein, waar vele Engelse scouts inzaten, naar Glasgow en Fort William. Hier werd de troep op het station ontvangen door 
        een Schotse doedelzakband. Na de busreis naar het kampterrein kon eindelijk gegeten en geslapen worden, Engelse scouts hadden het kamp al voor ons opgebouwd. Tijdens 
        een Engelse high tea op Achnacarry verleende Lochiel, de clanchief, de troep het recht om de Cameron-tartan in de modern colours te mogen dragen. Deze rood-groen-gouden 
        tartan is beschermd en slechts voorbehouden aan familie en degenen die toestemming van de clanchief hebben.
    </p>

    <p>
        In 1974 en 1975 groeide de troep enorm, zodat op kamp in Well met 36 verkenners gedraaid werd. In 1975 werd ook voor het eerst de strandhike gelopen. Deze eerste editie 
        ging per bakfiets naar Hoek van Holland. In alle latere edities ging de strandhike naar Den Helder en loopt het kader de strandhike zonder bakfiets. In 1976 werd door 
        de weer opgerichte rowans een nieuw onderkomen gebouwd, TNO. De reis naar het tweede Schotse kamp (1977) werd ditmaal per vliegtuig en bus gedaan. Tijdens het kamp werd 
        de Ben Nevis, de hoogste berg van Groot Brittannië, bedwongen. Het kampterrein was ditmaal op een veld in Clunes (5 huizen en een kraan), gelegen op een steenworp van 
        Achnacarry en Loch Lochy.
    </p>

    <p>
        In 1981 ging de troep met een aantal andere groepen uit het district Haarlem naar een groot internationaal kamp in Westerwald, Duitsland. De Mafeking-horde ging in 1982 
        naar Duitsland, als afsluiting van de periode dat akela Knape de horde leidde. Hij werd opgevolgd door akela Cathy Kniphorst. De troep vertrok in 1986 voor de derde maal 
        naar Schotland. Wederom met de boot en trein. Clunes was ook deze maal de kamp bestemming. De welpenhordes gingen datzelfde jaar allebei naar Maarn.
    </p>

    <p>
        De Impeesa-horde was intussen steeds verder geslonken en na het vertrek van akela Prevost werden in 1987 de laatste 3 welpen overgevlogen naar Kraantje Lek. De 
        Mafeking-horde onder akela Kniphorst groeide door tot 40 welpen. Met de Troep ging het minder goed, in 1988 gingen de verkenners met slechts twee patrouilles op kamp 
        naar Nieuw Bergen. Onder de nieuwe hopman Honoré Collet nam het aantal verkenners vanaf 1990 langzaam weer toe. Op nieuwjaarsdag 1991 brandde door brandstichting T.N.O., 
        tot de grond toe af.
    </p>

    <p>
        In 1992 deed de groep afstand van het clubhuis Wontolla aan de Randweg, omdat er geen uitzicht was op een nieuwe horde. De rowans wonnen in februari voor de derde maal op 
        rij de Winterhike van het district. Op 5 mei kwam er na veel geharrewar met de verzekering, een nieuw rowangebouw. De verkenners vertrokken voor de vierde maal in historie 
        naar Clunes in Schotland. Ook deze maal met boot en trein, met dien verstande dat de troep ditmaal een goederenwagon verbouwde tot slaapwagon.
    </p>

    <p>
        Eind 1992 werd onder leiding van het stafteam van de Camerons een 50 meter lange houten brug over het Spaarne gebouwd. Op de plaats waar nu de Nieuwe Lange Brug ("verfroller") 
        ligt. De welpen gingen als afsluiting van de periode dat akela Kniphorst de welpenhorde geleid had in 1993 op kamp naar de Vogezen in Frankrijk. Na dit kamp werd Fred Rijns 
        de nieuwe akela.
    </p>

    <p>
        In 1997 gingen de verkenners voor de vijfde maal naar Schotland op kamp, dit maal met drie personenbusjes. Er werd wederom gekampeerd in Clunes. Op 29 november 1997 werd een 
        nieuwe stam opnieuw opgericht, met daarin oud-staf en -leiding. De verkenners vertrokken in 2001 wederom naar Schotland (VI) en kregen een hartelijke ontvangst en rondleiding 
        van de Clanchief en zijn vrouw te Achnacarry House. In dit jaar, 2005, wonnen de verkenners niet alleen de RSW, maar ook de winterhike, deze wedstrijd voor rowans werd door 
        het kader van de verkenners gewonnen.
    </p>

    <p>
        In 2006 gingen de verkenners weer naar Schotland. Hier beklom voor het eerst de gehele troep de Ben Nevis, de hoogste berg van Schotland 
        en zouden weer vele excursies gemaakt worden. In 2007 wonnen de verkenners voor de derde maal achter elkaar de RSW. 
        Later werden zij zelfs derde op de Landelijke Scouting Wedstrijden (LSW). Na het welpenkamp in Apeldoorn van dat jaar bleek al gauw dat de 
        welpennesten door het stijgende aantal leden op springen stond, waardoor tegen het eind van het jaar nest Wit, na lange afwezigheid, weer 
        in het leven is geroepen.
    </p>

    <p>
        Het eerste verkenners weekend kamp van het nieuwe seizoen werd gehouden ten zuiden van Maastricht, net over de grens. 
        Hier overnachtte de verkenners in de mergelgroeven. Inmiddels is het een traditie geworden om dit eens in de 2 a 3 jaar te doen. 
        In de zomer van 2010 vertrok de troep voor de achtste maal naar Schotland. Ondanks de vele regen was dit een bijzonder geslaagd zomerkamp. 
        Aan het begin van het nieuwe seizoen won de Stam op de regionale roeiregatta de derde plaats. De hoogste plaats ooit behaald door een 
        landgroep. Een jaar later werden ze zelfs 2e. De Zwaluwen wonnen dat jaar de RSW. Op de LSW eindigde de Zwaluwen als 9e. 
        De Valken wonnen in 2012 wederom de RSW. Het was de vijfde keer dat het wisselschild naar ons ging en dus was deze nu definitief van ons.
    </p>

    <p>
        In de zomer van 2012 vertrokken de verkenners onder leiding van Hopman Marcel Glas voor het eerst naar Ierland om daar hun zomerkamp te 
        houden. Op een stuk terrein vlakbij een boerderij van de familie Goodman werd het kampement opgeslagen. Tijdens dit kamp waren er diverse 
        uitjes waaronder de beklimming van de hoogste berg van Ierland, de Carrauntoohill. Naar de top van deze berg (1039 meter) loopt geen 
        toeristisch pad wat het een echte beklimming maakte. Als locatie voor het zomerkamp 2014 van de verkenners was Schotland weer aan de beurt. 
        Tijdens dit negende Schotland kamp was het weer beter dan ooit en liep de troep in zijn geheel de Ben Nevis op. In 2015 wonnen de Rowans 
        voor de derde keer achter elkaar de winterhike en mochten de wisseltrofee nu definitief houden. In 2017 wonnen de Haviken de RSW en eindigde 
        op de LSW als 10e.
    </p>

    <p>
        Het zomerkamp van de welpen werd in Blitterswijck gehouden. Daar vlakbij in Nieuw-Bergen hielden de verkenners hun zomerkamp. 
        Het kamp stond in het thema van Indiana Jones en het was een kamp op palen. De patrouilles bouwden ieder een flat met op de eerste 
        verdieping (+/- 1,5 a 2 meter hoog) de keuken, bagagetent en vuurplaats en op de tweede verdieping de slaaptenten. Tussen de flats 
        waren apebanen gespannen zodat je van patrouille naar patrouille kon zonder de grond te raken.
    </p>

    <p>
        Na de zomer nam Akela Oudevrielink na 8 jaar Akela te zijn geweest afscheid van de horde. Jasper Leeuwenstein werd de nieuwe (en huidige) 
        Akela. Omdat de Troep inmiddels gegroeid was tot 35 man werd besloten om de Sperwers (voor het eerst sinds 1971) weer in het leven te roepen. 
        Het was voor het eerst sinds 1960 dat er weer 5 patrouilles waren en voor het eerst sinds de jaren 70 dat er meer dan 30 verkenners waren.
    </p>

    <p>
        Tijdens Pasen 2018 vertrok het kader naar het plaatsje Theux in de Belgische Ardennen voor een driedaags kaderweekend dat zij zelf 
        georganiseerd hadden. De staf ging ook mee en zou voornamelijk Belgische kampterreintjes bekijken.
    </p>

    <p>
        In de zomer van 2018 vertrokken de verkenners voor de tiende maal naar Schotland. De Troep zou met maar liefst 29 verkenners, 5 staf en 
        3 kampstaf in Clunes kamperen. Tijdens dit zomerkamp werden er op de Cameron Hill Bonnets uitgedeeld. Deze waren in 1997 (Schotland V) 
        ook al eens uitgedeeld maar werden sindsdien niet meer gemaakt. In de laatste week van dit zomerkamp gaf Hopman Marcel Glas het stokje 
        na 15 jaar over aan de nieuwe Hopman Finn Goodman. Op de Chia-Aig waterval werd Finn Goodman onder het oog van de staf en Ereraad geïnstalleerd.
    </p>

    <p>
        De strandhike van 2018 werd gelopen in goed weer. Maar liefst 17 mensen liepen de strandhike uit waaronder een record van 9 verkenners. 
        Acht hiervan waren voor de eerste keer gestart. Net zoals twee jaar eerder wisten de Haviken na de patrouille wedstrijden ook de RSW te winnen.
    </p>

